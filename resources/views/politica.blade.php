                                    <p class=MsoNormal style='margin-bottom:0cm'><b><span style='font-size:13.0pt;
                                    line-height:107%'>I. Introdução</span></b></p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>Sempre que cria uma conta pessoal na página de internet da Veidi ou
                                    instala e utiliza a aplicação Veidi, os seus dados pessoais (doravante
                                    designados apenas por “dados”) são-nos confiados. Agradecemos a confiança que
                                    deposita em nós e tudo faremos para justificar, manter e merecer essa
                                    confiança, pois a segurança e a correcção de tratamento dos seus dados é um
                                    pilar do funcionamento da Veidi que permite a análise e compreensão da nossa
                                    política de privacidade.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>A nossa política de privacidade estabelece os princípios orientadores da
                                    nossa actividade no que respeita à recolha, utilização e alteração dos seus
                                    dados e, assim sendo, a leitura desta política é recomendável para melhor
                                    compreender os seus direitos.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;line-height:normal'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'><b><span style='font-size:13.0pt;
                                    line-height:107%'>II. Enquadramento Geral</span></b></p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpFirst style='margin-bottom:0cm;text-indent:-18.0pt'><b>1.<span
                                    style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></b><b>Âmbito
                                    de Aplicação</b></p>

                                    <p class=MsoListParagraphCxSpLast style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>A política de privacidade da Veidi aplica-se aos utilizadores dos
                                    serviços disponibilizados por esta empresa em qualquer parte de Portugal,
                                    independentemente do meio informático utilizado, incluindo as aplicações e/ou
                                    dos websites dos serviços e funcionalidades Veidi.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>Esta política define a forma como a Veidi recolhe e utiliza os seus dados
                                    e aplica-se a todos os colaboradores Veidi que tenham acesso aos seus dados.
                                    Para efeitos do presente documento, entende-se por:</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify'>&nbsp;</p>

                                    <p class=MsoListParagraph style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'><b>a)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span></b><b>Utilizadores:</b> todas as pessoas, singulares ou colectivas, que
                                    pedem ou recebem serviços prestados pelos colaboradores da Veidi através das
                                    suas contas Veidi;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpFirst style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'><b>b)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span></b><b>Parceiros Veidi</b>: conjunto de pessoas, singulares ou
                                    colectivas, com competência legal e meios próprios para transporte terrestre de
                                    mercadorias e/ou realização de mudanças, que celebraram um acordo de parceria
                                    com a Veidi;</p>

                                    <p class=MsoListParagraphCxSpMiddle><b>&nbsp;</b></p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'><b>c)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span></b><b>Motoristas:</b> pessoas singulares que fornecem serviços Veidi aos
                                    Utilizadores e que têm um vínculo jurídico com as empresas de transporte de
                                    mercadorias parceiras da Veidi;</p>

                                    <p class=MsoListParagraphCxSpMiddle>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'><b>d)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span></b><b>Ajudantes de Motoristas</b>: pessoas singulares que acompanham e
                                    auxiliam os Motoristas na prestação dos serviços Veidi, sempre que os Utilizadores
                                    contratem o serviço com Motorista e Ajudante;</p>

                                    <p class=MsoListParagraphCxSpMiddle>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpLast style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'><b>e)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span></b><b>Utilizadores Convidados:</b> pessoas que recebem serviços Veidi por
                                    outros proprietários de contas Veidi, incluindo amigos, familiares ou outros
                                    proprietários de contas individuais.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    normal'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>A nossa política de privacidade rege-se pelas leis aplicáveis aos locais
                                    onde a Veidi disponibiliza os seus serviços, independentemente do local onde os
                                    mesmos são solicitados pelos Utilizadores.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpFirst style='margin-bottom:0cm;text-indent:-18.0pt'><b>2.<span
                                    style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></b><b>Responsabilidade
                                    Pela Recolha, Tratamento e Segurança de Dados</b></p>

                                    <p class=MsoListParagraphCxSpLast style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>A Veidi Portugal, Lda. (<a href="https://veidi.pt/">https://veidi.pt/</a>)
                                    é a empresa responsável pela recolha, tratamento e segurança dos dados
                                    recolhidos em Portugal. Atenta a área territorial que, inicialmente, a Veidi
                                    irá dedicar a actividade, não está prevista a possibilidade de transferência de
                                    dados pessoais para fora do território português.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>No entanto, a Veidi respeita o <b>princípio da necessidade</b> para a
                                    transmissão de dados pessoais e, assim sendo, logo que a Veidi possa operar
                                    noutros países e a transferência dos seus dados seja necessária para que possa
                                    utilizar os serviços Veidi noutro país ou para outro país, estes dados (e
                                    apenas os necessários a cumprir tal fim) poderão ser transmitidos para países
                                    terceiros, para que possa utilizar os serviços Veidi sem quaisquer limitações.
                                    Neste caso, também tais transmissões serão objecto de protecção de dados, quer esteja
                                    no seu país de residência ou noutro local.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    normal'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>Caso seja necessário transferir para outro país, se tal for necessário
                                    para responder a um pedido de apoio ao cliente enviado fora de Portugal ou na
                                    eventualidade do Utilizador deseje aceder ao seu histórico de serviços Veidi
                                    quando se encontra ausente do território nacional, tal transmissão de dados
                                    será realizada atento o fim a que se destina e por ser um meio necessário para
                                    satisfazer o pedido do próprio titular dos dados enviados.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>Por fim, a Veidi poderá disponibilizar dados para outros países caso seja
                                    destinatária de um pedido de acesso a dados pessoais realizado por entidade
                                    policial e/ou judicial competente, desde que tal pedido obedeça aos requisitos
                                    legais vigentes em Portugal, bem como o Regime Geral de Protecção de Dados
                                    (RGPD) em vigor na União Europeia. Para análise, tratamento e resposta a estes
                                    pedidos de transferências de dados, a Veidi implementou um procedimento interno
                                    vigoroso e garantístico, a fim de acautelar o respeito pelos direitos pessoais
                                    dos seus utilizadores. aplica um procedimento interno robusto para processar
                                    estes pedidos.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>Para permitir a protecção dos dados pessoais que lhe são confiados, a
                                    Veidi implementou um conjunto de medidas cautelares, a saber:</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpFirst style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt'>a)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span>Definição de políticas e procedimentos para limitar o acesso a e o
                                    processamento de dados pessoais para fins definidos;</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-bottom:0cm;text-align:justify'>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt'>b)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span>Formação específica do pessoal responsável pela gestão de dados
                                    pessoais;</p>

                                    <p class=MsoListParagraphCxSpMiddle>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'>c)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span>Implementação de uma política empresarial de impedimento de acesso
                                    governamental e/ou policial aos dados dos utilizadores, excepto quando a
                                    divulgação for obrigatória pelas leis aplicáveis, incluindo o RGPD da UE, se
                                    houver risco iminente e fundamentado da verificação de danos pessoais e/ou
                                    patrimoniais graves ou se obtenha consentimento do titular desses dados;</p>

                                    <p class=MsoListParagraphCxSpMiddle>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpLast style='margin-bottom:0cm;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'>d)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span>Implementação de medidas de protecção robusta dos dados enquanto estão
                                    em trânsito entre as aplicações Veidi e os nossos servidores e quando os dados
                                    são processados nos nossos servidores. Isto inclui a encriptação, anonimização
                                    ou pseudonimização destes dados, a fim de se salvaguardar a identidade dos
                                    utilizadores relativamente à divulgação não autorizada dos seus dados.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;line-height:normal'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>Sempre que os Utilizadores tiverem comentários, reclamações, questões ou
                                    dúvidas que visem esclarecer ou desejem exercer os direitos que lhes são
                                    reconhecidos em matéria de protecção de dados pessoais, deverão encaminhar as suas
