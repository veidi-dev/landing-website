                                    <p class=MsoListParagraphCxSpFirst style='margin-bottom:0cm;text-indent:-18.0pt'><b>3.<span
                                    style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></b><b>Recolha
                                    e Utilização de Dados</b></p>

                                    <p class=MsoListParagraphCxSpLast style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>A Veidi recolhe dados desde o momento em que se cria uma conta Veidi ou
                                    se pede um serviço Veidi, até à respectiva conclusão do mesmo e sempre que a
                                    aplicação está em execução em primeiro plano (a aplicação está aberta e aparece
                                    no ecrã).</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;line-height:normal'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>Os Utilizadores e Destinatários de serviços Veidi podem usar as
                                    aplicações desta empresa sem permitir a recolha de dados de localização dos
                                    respectivos dispositivos móveis. Contudo, isto poderá afectar algumas
                                    funcionalidades destas aplicações. Por exemplo, um Utilizador que não active os
                                    dados da localização precisa do seu dispositivo terá de introduzir manualmente
                                    o endereço de recolha caso este coincida com o lugar onde esteja no momento em
                                    que realiza o pedido de serviço.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;line-height:normal'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm;text-align:justify;line-height:
                                    150%'>Os dados de localização precisa recolhidos a partir dos dispositivos dos Motoristas,
                                    durante as suas deslocações para prestação do serviço solicitado pelo Utilizador
                                    são associados à conta deste, mesmo que não tenha activado a recolha de dados
                                    da localização precisa dos respectivos dispositivos. Estes dados são utilizados
                                    para fins de geração de recibos, apoio ao cliente, detecção de fraudes, seguro
                                    e litígios.</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>&nbsp;</p>

                                    <p class=MsoNormal style='margin-bottom:0cm'>A Veidi recolhe:</p>

                                    <p class=MsoListParagraphCxSpFirst>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-top:0cm;margin-right:0cm;
                                    margin-bottom:0cm;margin-left:54.0pt;text-align:justify;text-indent:-18.0pt;
                                    line-height:150%'>a)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span><b>Dados de utilização:</b> informação que diz respeito à forma como os
                                    utilizadores interagem com o <i>software</i> disponibilizado pela Veidi para
                                    aquisição dos serviços disponibilizados por esta, incluindo, mas não
                                    exaustivamente, as datas, horas de acesso e respectivo local, funcionalidades
                                    da aplicação visualizadas, o tipo de <i>browser</i> usado pelo Utilizador e os
                                    relatórios de falhas da aplicação que, por qualquer razão, venham a ser gerados.
                                    Dentro destes dados estão incluídos dos dados fornecidos pelos Utilizadores
                                    aquando da criação, actualização e utilização de uma conta Veidi, bem como,
                                    entre outros, a geolocalização, a morada de início e destino do serviço, os
                                    dados do dispositivo utilizado e o valor do serviço, o número de contribuinte
                                    do Utilizador, o seu e-mail, número de telefone, nome, palavra-passe para
                                    acesso à conta Veidi e fotografia de perfil (se disponibilizada pelo Utilizador);</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-top:0cm;margin-right:0cm;
                                    margin-bottom:0cm;margin-left:54.0pt;text-align:justify;line-height:normal'>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-left:54.0pt;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'>b)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span><b>Dados do dispositivo:</b> informação relativa aos dispositivos
                                    utilizados para aceder aos nossos serviços, incluindo os modelos de hardware, o<i>
                                    software</i> associado ao mesmo, incluindo sistema operativo e sua versão, o
                                    endereço IP ou outros identificadores únicos do dispositivo, os idiomas
                                    preferidos, os identificadores de publicidade, os dados de movimento do
                                    dispositivo e os dados da rede móvel;</p>

                                    <p class=MsoListParagraphCxSpMiddle>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-top:0cm;margin-right:0cm;
                                    margin-bottom:0cm;margin-left:54.0pt;text-align:justify;text-indent:-18.0pt;
                                    line-height:150%'>c)<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                    </span><b>Informações de transacção:</b> a Veidi recolhe e guarda as informações
                                    de todas as transacções realizadas pelos Utilizadores na utilização dos
                                    serviços Veidi, incluindo o tipo de serviços solicitados ou fornecidos,
                                    detalhes de transportes solicitados (como data e hora solicitada, bem como da
                                    prestação do serviço, caso o serviço solicitado necessite de agendamento prévio,
                                    endereços de recolha e entrega das mercadorias e distância percorrida para a
                                    prestação do serviço), bem como as informações relativa aos pagamentos desses
                                    serviços, como os valores pagos e respectiva forma de pagamento;</p>

                                    <p class=MsoListParagraphCxSpMiddle>&nbsp;</p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-left:54.0pt;text-align:justify;
                                    text-indent:-18.0pt;line-height:150%'><span style='background:yellow'>d)<span
                                    style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><b>Dados
                                    relativos aos parceiros Veidi:</b> estes dados são fornecidos directamente e de
                                    forma livre e esclarecida, pelos parceiros Veidi quando celebram um acordo de
                                    parceria com a Veidi. Nestes incluem-se a identificação dos parceiros e
                                    respectivos alvarás para a actividade de transporte terrestre de mercadorias,
                                    bem como os documentos oficiais relativos aos veículos utilizados nos serviços
                                    Veidi (incluindo a identificação de apólices em vigor) e os respeitantes aos
                                    seus motoristas (como identificação destes e os documentos relativos à sua
                                    habilitação legal para conduzir) e ajudantes de motorista. <span
                                    style='background:yellow'>Na recolha dos dados destes fornecedores incluem-se
                                    ainda informações dos contactos de emergência dos motoristas e ajudantes e
                                    declaração médica atestando a aptidão destes para a realização dos serviços
                                    Veidi;</span></p>

                                    <p class=MsoListParagraphCxSpMiddle><b>&nbsp;</b></p>

                                    <p class=MsoListParagraphCxSpMiddle style='margin-top:0cm;margin-right:0cm;
                                    margin-bottom:0cm;margin-left:54.0pt;text-align:justify;text-indent:-18.0pt;
